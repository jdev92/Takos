<div class="infoTarif">
    <h2>Les tarifs du taxi parisien</h2>
    <p>Le prix d'une course en taxi est déterminé par le compteur horokilométrique (taximètre) qui équipe
        obligatoirement chaque taxi.</p>
    <p>Le principe de fonctionnement du taximètre est identique à tous les taxis mais selon qu'il s'agit des
        taxis parisiens ou des taxis de province l'application des termes kilométriques et horaires diffère.</p>
    <p>Le montant de la prise en charge est de 2,60 €, quelle que soit la course. <br> Tarif A : 1,09 € le km -
        Tarif B : 1,38 € e le km - Tarif C : 1,61 € le km <br> Heure d'attente : Tarif A : 33,48 € l'heure -
        Tarif B : 42,41 € l'heure - Tarif C : 35,99 € l'heure</p>
    <p>Le détail des tarifs et de leurs applications sont définis par arrêté préfectoral pour chaque
        département.</p>
    <ul>
        <li>JOUR/NUIT : c'est la prise en charge, soit le montant de départ du compteur lors de sa mise en
            marche.</li>
        <li>KM : tarif pour un kilomètre</li>
        <li>HEURE : tarif horaire, il s'enclenche automatiquement dès que le compteur est en marche et que le
            véhicule est à l'arrêt (embouteillage, attente du client) ou en alternance avec le tarif
            kilométrique quand la vitesse atteint le point de conjonction des tarifs horaire et kilométrique
            (vitesse du véhicule inférieure à : tarif horaire divisé par le tarif kilométrique appliqué), dans
            ce cas la comptabilisation par le tarif kilométrique s'arrête.</li>
    </ul><br>

    <h2>Taxi Parisien :</h2>
    <ul>
        <li>KM A et Heure A :</li>
        <li>- applicable de 10h à 17h dans Paris intra-muros boulevard périphérique inclus.</li>
        <li>KM B et Heure B :</li>
        <li>- applicable dans Paris intra-muros boulevard périphérique inclus de 17h à 10h</li>
        <li>- applicable dans Paris intra-muros boulevard périphérique inclus de 7h à 24h les dimanches</li>
        <li>- applicable dans Paris intra-muros boulevard périphérique inclus de 0h à 7h les jours fériés</li>
        <li>- applicable de 7h à 19h tous les jours dans la zone suburbaine (la zone suburbaine étant composée
            des communes extérieures à Paris mais rattachées aux taxis parisiens)</li>
        <li>KM C et Heure C :</li>
        <li>- applicable dans Paris intra-muros boulevard périphérique inclus de 0h à 7h les dimanches</li>
        <li>- applicable dans la zone suburbaine de 19h à 7h</li>
        <li>- applicable dans la zone suburbaine à toutes heures les dimanches et jour fériés</li>
        <li>- applicable tous les jours à toute heure au-delà de la zone suburbaine.</li>
        <li>Quel que soit le montant affiché au compteur la somme à payer ne peut être inférieure à 7.30 €</li>

        <li> Des suppléments tarifaires sont applicables pour le transport de passager supplémentaire à partir
            du 5ème ( 4 € ), pour les réservations et pour les bagages au dela de trois bagages par passager ou
            pour
            tout bagage nécessitant l'utilisation d'un équipement extérieur.</li>
        <li>Depuis le 1er mars 2016 des forfaits sont applicables pour la desserte des aéroports d'Orly et de
            Roissy au départ ou à l'arrivée de Paris intra-muros.</li>
    </ul>
</div>

